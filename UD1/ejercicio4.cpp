/* Algoritmo que muestra una operacion aritmética de dos variables. */

#include <iostream>
using namespace std;

int main() {
    
    //Declaración e iniciación de variables.
    double a, b;
    a=5, b=3;
    
    //Muestra el resultado de la operación por pantalla.
    cout << 5 + a * b + a / b * 4 << endl;
    
    return 0;
}
